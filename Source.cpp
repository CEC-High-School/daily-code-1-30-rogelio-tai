#include <iostream>
using namespace std;

int main() {

	for (int i = 20; i < 81;i++)
		cout << i << endl;

	for (int i = 70; i > 9; i--)
		cout << i << endl;

	int i = 20;
	while (i < 81) {
		cout << i << endl;
		i++;
	}

	int l = 70;
	while (l >= 10) {
		cout << l << endl;
		l--;
	}

}